export const t = (
  key: string,
  lang: string = 'en',
  vars?: Record<string, string | number>,
): string => {
  const langMap = translations[lang] ?? translations['en'] ?? {};
  const enMap = translations['en'] ?? {};
  let value = langMap[key] ?? enMap[key] ?? key;

  if (vars) {
    Object.keys(vars).forEach((k) => {
      value = value.replace(new RegExp(`{${k}}`, 'g'), String(vars[k]));
    });
  }

  return value;
};

export default t;
